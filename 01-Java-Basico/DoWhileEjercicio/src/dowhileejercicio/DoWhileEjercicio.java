
package dowhileejercicio;


public class DoWhileEjercicio {

 
    public static void main(String[] args) {
        //Contar de 0 a 10 con Do While
        
        int cont = 0;
        do{ 
            System.out.println("Estoy en el numero:" + cont); 
            cont = cont + 1;
           
        } while (cont<=10);
       
        
    }
    
}
